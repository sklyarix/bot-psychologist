// простейший рендерер {{var}}
export function renderTemplate(tpl, vars = {}) {
	return String(tpl ?? '').replace(/\{\{\s*(\w+)\s*\}\}/g, (_, k) =>
		vars[k] == null ? '' : String(vars[k])
	)
}

/**
 * Собирает итоговые настройки для вызова:
 */
export function buildAIRequest({ presetKey, text }) {
	const preset = AI_CONFIG.presets[presetKey]
	if (!preset) throw new Error(`AI preset not found: ${presetKey}`)

	const system = preset.system

	// если есть шаблон — подставляем {{text}}, иначе просто берём text
	const userFromTemplate = preset.userTemplate
		? renderTemplate(preset.userTemplate, { text })
		: text

	return [
		{ role: 'system', content: system },
		{ role: 'user', content: userFromTemplate }
	]
}
